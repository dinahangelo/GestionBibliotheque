    private void dateFinPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_dateFinPropertyChange
        // TODO add your handling code here:
        java.util.Date utilDateD = dateDebut.getDate();
        java.util.Date utilDateF = dateFin.getDate();

        if(utilDateD != null && utilDateF != null) {
            java.sql.Date dateD = new java.sql.Date(utilDateD.getTime());
            java.sql.Date dateF = new java.sql.Date(utilDateF.getTime());

            if(dateD.after(dateF)) {
                JOptionPane.showMessageDialog(null, "La date début ne doit pas être postérieure à la date fin");
                dateFin.setDate(null);
            }
        }
    }//GEN-LAST:event_dateFinPropertyChange

