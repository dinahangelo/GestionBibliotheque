    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int nbLivreExistant = 0;
        int nbLivreEmprunter = 0;
        for(Livre l : gl.getLivreById(codeLivre)){
            nbLivreExistant = l.getNombreLivre();
        }
        nbLivreEmprunter = gem.getNombreLivre(codeLivre);
        insertion = !((nbLivreExistant-nbLivreEmprunter) < Integer.parseInt(nombreLivre.getValue().toString()) || Integer.parseInt(nombreLivre.getValue().toString())<0);
        
        if(dateDebut.getDate() == null || dateFin.getDate() == null || "".equals(txtLivre.getText())){
            if("".equals(txtLivre.getText())){
                JOptionPane.showMessageDialog(null, "Veuillez choisir un livre");
            }else if(dateDebut.getDate() == null){
                JOptionPane.showMessageDialog(null, "La date début ne peut pas être null");
            }else{
                JOptionPane.showMessageDialog(null, "La date fin ne peut pas être null");                
            }
        }else if("Choisir".equals(comboEmprunteur.getSelectedItem().toString())){
            JOptionPane.showMessageDialog(null, "Veuillez choisir l'emprunteur");
        }else if(!testEmprunteur){
            if(JOptionPane.showConfirmDialog(null, "Cet emprunteur n'a pas encore retourner un livre !, Voulez-vous continuer ?") == JOptionPane.YES_OPTION){
                if(!insertion){
                    if(Integer.parseInt(nombreLivre.getValue().toString())<0){
                        JOptionPane.showMessageDialog(null, "Le nombre du livre demandé doit être positif!");
                    }else{
                        JOptionPane.showMessageDialog(null, "Veuillez vérifier le nombre de livre disponible et le livre demandé !");                    
                    }
                }else{
                    if("Professeur".equals(comboEmprunteur.getSelectedItem().toString())){
                        gem.insert(new Emprunter(idEmprunt, emprunteurGettedSplitted[0]+"-"+emprunteurGettedSplitted[1], codeLivre, new java.sql.Date(dateDebut.getDate().getTime()), new java.sql.Date(dateFin.getDate().getTime()), classe1.getSelectedItem().toString()+" "+classe2.getSelectedItem().toString(), txtAnneeScolaire.getText(), Integer.parseInt(nombreLivre.getValue().toString()), "En cours"));
                        dispose();
                    }else{
                        if("  ".equals(classe1.getSelectedItem().toString()+classe2.getSelectedItem().toString())){
                            JOptionPane.showMessageDialog(null, "Veuillez choisir la classe !");
                        }else{
                            gem.insert(new Emprunter(idEmprunt, emprunteurGettedSplitted[0], codeLivre, new java.sql.Date(dateDebut.getDate().getTime()), new java.sql.Date(dateFin.getDate().getTime()), classe1.getSelectedItem().toString()+" "+classe2.getSelectedItem().toString(), txtAnneeScolaire.getText(), Integer.parseInt(nombreLivre.getValue().toString()), "En cours"));
                            dispose();
                        }
                    }
                } 
            }
        }else{
            if(!insertion){
                if(Integer.parseInt(nombreLivre.getValue().toString())<0){
                    JOptionPane.showMessageDialog(null, "Le nombre du livre demandé doit être positif!");
                }else{
                    JOptionPane.showMessageDialog(null, "Veuillez vérifier le nombre de livre disponible et le livre demandé !");                    
                }
            }else{
                if("Professeur".equals(comboEmprunteur.getSelectedItem().toString())){
                    gem.insert(new Emprunter(idEmprunt, emprunteurGettedSplitted[0]+"-"+emprunteurGettedSplitted[1], codeLivre, new java.sql.Date(dateDebut.getDate().getTime()), new java.sql.Date(dateFin.getDate().getTime()), classe1.getSelectedItem().toString()+" "+classe2.getSelectedItem().toString(), txtAnneeScolaire.getText(), Integer.parseInt(nombreLivre.getValue().toString()), "En cours"));
                    dispose();
                }else{
                    if("  ".equals(classe1.getSelectedItem().toString()+classe2.getSelectedItem().toString())){
                        JOptionPane.showMessageDialog(null, "Veuillez choisir la classe !");
                    }else{
                        gem.insert(new Emprunter(idEmprunt, emprunteurGettedSplitted[0], codeLivre, new java.sql.Date(dateDebut.getDate().getTime()), new java.sql.Date(dateFin.getDate().getTime()), classe1.getSelectedItem().toString()+" "+classe2.getSelectedItem().toString(), txtAnneeScolaire.getText(), Integer.parseInt(nombreLivre.getValue().toString()), "En cours"));
                        dispose();
                    }
                }
            } 
        }
    }//GEN-LAST:event_jButton1ActionPerformed

