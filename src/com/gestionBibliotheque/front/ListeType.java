    public void listType(){
        DefaultTableModel table = new DefaultTableModel();
        table.addColumn("Id");
        table.addColumn("Libelle");
        tableType.setModel(table);
        TableColumnModel colonne = tableType.getColumnModel();
        colonne.getColumn(0).setPreferredWidth(50);
        colonne.getColumn(1).setPreferredWidth(250);
        for(com.gestionBibliotheque.classe.Type t : gt.list()){
            Object[] line = {t.getIdType(), t.getLibelleType()};
            table.addRow(line);
        }
    }
    
    public void search(){
        DefaultTableModel table = new DefaultTableModel();
        table.addColumn("Id");
        table.addColumn("Libelle");
        tableType.setModel(table);
        TableColumnModel colonne = tableType.getColumnModel();
        colonne.getColumn(0).setPreferredWidth(50);
        colonne.getColumn(1).setPreferredWidth(250);
        for(com.gestionBibliotheque.classe.Type t : gt.search(txtSearch.getText())){
            Object[] line = {t.getIdType(), t.getLibelleType()};
            table.addRow(line);
        }
    }

