    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            if(PasswordEncryptor.encryptPassword(txtPasswordActuel.getText()).equals(PasswordEncryptor.encryptPassword(txtNouveauPassword.getText()))){
                JOptionPane.showMessageDialog(null, "Le nouveau mot de passe doit être différent du mot de passe actuel.");
            }else if(PasswordEncryptor.encryptPassword(txtNouveauPassword.getText()).equals(PasswordEncryptor.encryptPassword(txtConfirmerPassword.getText()))){
                gu.update(new Utilisateur("lp-kmm", PasswordEncryptor.encryptPassword(txtNouveauPassword.getText())), PasswordEncryptor.encryptPassword(txtPasswordActuel.getText()));
            }else{
                JOptionPane.showMessageDialog(null, "Veuillez vérifier votre mot de passe !");
            }
        } catch (HeadlessException | NoSuchAlgorithmException e) {
            System.err.println("Modification password error : "+e);
        }
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

